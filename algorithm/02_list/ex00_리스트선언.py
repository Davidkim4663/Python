"""
[리스트 선언]
 a = [10, 20, 30, 40]
 위와 같이 선언하면 변수 4개를 하나의 묶음으로 변수 a에 저장된다.
 각각의 값을 사용하기 위해서는 index를 사용해 접근한다.

 방 번호(index)는 0부터 1씩 증가한다.
 즉, 위 리시트는 0번부터 3번까지의 index가 있는 것 이다.
"""
a = [1, 2, 3, 4, 5]
print(a)

print("1 = ", a[0])
print("2 = ", a[1])
print("3 = ", a[2])
print("4 = ", a[3])
print("5 = ", a[4])